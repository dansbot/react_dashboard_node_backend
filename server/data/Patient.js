const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const patientSchema = new Schema({
  patient_id: {
    type: String,
    required: true,
    unique: true,
  },
  diagnosis: String,
  ritmi: String,
  age: Number,
  sex: String,
  height: Number,
  weight: Number,
  ecg_id: Number,
  device: String,
  recording_date: String,
  report: String,
  pacemaker: String,
  nurse: Number,
  site: Number,
  scp_codes: String,
  heart_axis: String,
  infarction_stadium1: String,
  infarction_stadium2: String,
  validated_by: String,
  second_opinion: String,
  initial_autogenerated_report: String,
  validated_by_human: String,
  baseline_drift: String,
  static_noise: String,
  burst_noise: String,
  electrodes_problems: String,
  extra_beats: String,
  strat_fold: Number,
});

module.exports = mongoose.model("Patient", patientSchema);
